inline int read(){
	int x=0,f=1;char c=getchar();
	for(;!isdigit(c);c=getchar())if(c=='-')f=-1;
	for(;isdigit(c);c=getchar())x=(x<<3)+(x<<1)+(c^48);
	return x*f;
}
int n,m;
int w[30];
int ans=-inf;
int co(int x)
{
    int cnt=0;
    for(int i=0;i<=n-1;++i)
        if(x&(1<<i))
            cnt++;
    return cnt;
}
int main()
{
    cin>>n>>m;
    for(int i=0;i<n;++i){
        cin >> w[i];
    }
    for(int i=0; i<=(1<<n)-1;++i){
        if(co(i)==n-m){
            bitset<2010>ANS;
            ANS[0]=1;
            for(int j=0;j<=n-1;++j){
                if(i&(1<<j)){
                    ANS=ANS|ANS<<w[j];
                }
            }
            ans=max(ans,(int)ANS.count());
        }
    }
    cout<<ans-1<<endl;
    return 0;
}
